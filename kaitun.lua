            displayText = displayText .. string.format("%s (%s)\nParent: %s\nValue: %s\n\n", 
                data.Name, data.Type, data.Parent, tostring(data.Value))
            if i >= 5 then break end
        end
        BondsInfo.Text = displayText
    else
        BondsInfo.Text = "No Treasury Bonds data found.\nSearching in player stats..."
        
        if player.leaderstats then
            for _, stat in pairs(player.leaderstats:GetChildren()) do
                if stat.Name:lower():find("bond") then
                    BondsInfo.Text = string.format("Found: %s\nValue: %s", stat.Name, stat.Value)
                    break
                end
            end
        end
    end
end

